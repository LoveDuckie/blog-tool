def get_default_user_config_filename() -> str:
    """Retrieve the default configuration file name for the user configuration

    Returns:
        str: Returns the filename for the user configuration
    """
    return "config_user.ini"


def get_default_config_filename() -> str:
    """Retrieve the default configuration file name

    Returns:
        str: The filename for the default configuraiton file
    """
    return "config_default.ini"
